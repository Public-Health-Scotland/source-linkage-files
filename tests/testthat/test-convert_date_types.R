test_that("Can convert a date type to numeric", {
  dates <- as.Date(c(
    "2017/09/13",
    "2018/03/12",
    "2018/03/01",
    "2018/07/06",
    "2019/03/13",
    "2018/09/04",
    "2019/05/27",
    "2020/05/29",
    "2020/04/28",
    "2019/05/24"
  ))

  expect_equal(
    convert_date_to_numeric(dates),
    c(
      20170913,
      20180312,
      20180301,
      20180706,
      20190313,
      20180904,
      20190527,
      20200529,
      20200428,
      20190524
    )
  )
})

test_that("Can convert a numeric 'date' to a proper date type", {
  numeric_dates <- c(
    20170913,
    20180312,
    20180301,
    20180706,
    20190313,
    20180904,
    20190527,
    20200529,
    20200428,
    20190524
  )

  expect_equal(
    convert_numeric_to_date(numeric_dates),
    as.Date(c(
      "2017/09/13",
      "2018/03/12",
      "2018/03/01",
      "2018/07/06",
      "2019/03/13",
      "2018/09/04",
      "2019/05/27",
      "2020/05/29",
      "2020/04/28",
      "2019/05/24"
    ))
  )
})
